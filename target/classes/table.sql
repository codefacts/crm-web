AUDITOR_NAME varchar(1000) DEFAULT ('') ,
AUDITOR_CODE varchar(100) DEFAULT ('') ,
CLUSTER_NAME varchar(1000) DEFAULT ('') ,
TSR_CODE varchar(100) DEFAULT ('') ,
TSR_NAME varchar(1000) DEFAULT ('') ,
